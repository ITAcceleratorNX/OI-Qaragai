const DETAIL_PATHS = {
  'Отели': 'hotels',
  'Рестораны': 'restaurants',
  'Развлечения': 'activities',
  SPA: 'spa',
};

const DETAIL_PATHS_BY_KEY = {
  hotels: 'hotels',
  restaurants: 'restaurants',
  fun: 'activities',
  spa: 'spa',
};

export function getDetailPath(type, id) {
  const segment = DETAIL_PATHS[type] ?? DETAIL_PATHS_BY_KEY[type];
  return segment ? `/${segment}/${id}` : null;
}

export function findById(section, id) {
  const detail = catalogs[section]?.find((item) => item.id === id);
  if (!detail) return null;
  const card = findCard(RAW_OQ.thingsAll, id);
  return {
    ...card,
    ...detail,
    name: detail.name ?? card?.title,
  };
}
